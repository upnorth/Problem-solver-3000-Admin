    private void btnAddTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTaskActionPerformed
        // TODO add your handling code here:
        //        String student = txtFname.getText() + " " + txtEname.getText() + " " + txtIdeal.getText();
        //        listModel.addElement(student);

        Task t = new Task();
        t.settaskCat(Integer.parseInt(taskCat.getText()));
        t.settaskEst(Integer.parseInt(taskEst.getText()));
        t.settaskPrio(Integer.parseInt(taskPrio.getText()));
        t.settaskTech(Integer.parseInt(taskTech.getText()));
        t.settaskCom(taskCom.getText());
        tasks.add(t);
        String task = "Tech: " + t.gettaskTech() + ", category: " + t.gettaskCat() + ", comments: " + t.gettaskCom();
        listModel.addElement(task);
        int index = listModel.getSize();
        if (index > 0){ //enable delete .
            btnDeleteTask.setEnabled(true);
            if (index == listModel.getSize()){
            index--;}
            lstTasks.setSelectedIndex(index);
            lstTasks.ensureIndexIsVisible(index);
        }
    }//GEN-LAST:event_btnAddTaskActionPerformed

