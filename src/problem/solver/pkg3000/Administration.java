    private void btnDeleteTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteTaskActionPerformed
        // TODO add your handling code here:
        int index = lstTasks.getSelectedIndex();
        listModel.remove(index);
        tasks.remove(index);
        int size = listModel.getSize();
        if (size == 0) { //Nobody's left, disable delete .
            btnDeleteTask.setEnabled(false);
        } else { //Select index.
            if (index == listModel.getSize()) {
                index--;
            }
            lstTasks.setSelectedIndex(index);
            lstTasks.ensureIndexIsVisible(index);
        } 
    }//GEN-LAST:event_btnDeleteTaskActionPerformed

    private void btnAddProbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProbActionPerformed
        // Add new problem to database
        Problem p = new Problem();
        p.setprobManager(Integer.parseInt(probManager.getText()));
        p.setprobClient(Integer.parseInt(probClient.getText()));
        p.setprobCat(Integer.parseInt(probCat.getText()));
        p.setprobPriority(Integer.parseInt(probPrio.getText()));
        p.setprobCom(taskCom.getText());
        try {
            p.addProblem();
        } catch (SQLException ex) {
            Logger.getLogger(Administration.class.getName()).log(Level.SEVERE, null, ex);
        }
        // Get new problem ID
        int problemID = p.newProblem();
        // Add problem ID to related tasks
        tasks.stream().forEach((t) -> {
            t.settaskParent(problemID);
        });
        // Add related tasks to database
        Task save = new Task();
        try {
            save.addTasks(tasks);
        } catch (SQLException ex) {
            Logger.getLogger(Administration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddProbActionPerformed

