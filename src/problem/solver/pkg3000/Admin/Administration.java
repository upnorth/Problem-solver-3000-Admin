    private void getManagerProblemsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getManagerProblemsActionPerformed
        int manager = Integer.parseInt(managerIdInput.getText());
        try {
            manageProblems = p.getProblems(manager);
        } catch (SQLException ex) {
            Logger.getLogger(Administration.class.getName()).log(Level.SEVERE, null, ex);
        }
        listModelP.clear();
        listModelPT.clear();
        this.clearInput();
        for(Problem c: manageProblems){
            String problem = "ID: " + c.getId() + ". Status: " + c.getStatus() + ". Priority: " + c.getPri() + ". Comments: " + c.getCom();
            listModelP.addElement(problem);
        }
    }//GEN-LAST:event_getManagerProblemsActionPerformed

    private void currentProblemsListMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_currentProblemsListMouseReleased
        int index = currentProblemsList.getSelectedIndex();
        listModelPT.clear();
        this.clearInput();
        manageProblems.get(index).tasks.stream().map((c) -> "ID: " + c.getId() + ". Status: " + c.getStatus() + ". Priority: " + c.getPri() + ". Comments: " + c.getCom()).forEach((String tasks) -> {
            listModelPT.addElement(tasks);
        });
        currentProblemCom.setText(manageProblems.get(index).getCom());
        currentProblemMan.setText(Integer.toString(manageProblems.get(index).getMan()));
        currentProblemPri.setText(Integer.toString(manageProblems.get(index).getPri()));
    }//GEN-LAST:event_currentProblemsListMouseReleased

