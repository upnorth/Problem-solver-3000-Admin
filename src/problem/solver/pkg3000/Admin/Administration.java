    private void updateProblemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateProblemActionPerformed
        int index = currentProblemsList.getSelectedIndex();
        int stat = currentProblemStatus.getSelectedIndex();
        switch (stat) {
            case 0:
                manageProblems.get(index).setStatus("Finished");
                break;
            case 1:
                manageProblems.get(index).setStatus("In progress");
                break;
            case 2:
                manageProblems.get(index).setStatus("Aborted");
                break;
            default:
                break;
        }
        manageProblems.get(index).setMan(Integer.parseInt(currentProblemMan.getText()));
        manageProblems.get(index).setPri(Integer.parseInt(currentProblemPri.getText()));
        manageProblems.get(index).setCom(currentProblemCom.getText());
        try {
            String save = manageProblems.get(index).updateProblem(manageProblems.get(index));
            // TODO add your handling code here:
        } catch (SQLException ex) {
            Logger.getLogger(Administration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_updateProblemActionPerformed

